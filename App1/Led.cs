using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;
using GrovePi;
using GrovePi.Sensors;

namespace App1
{
    public class Led
    {
        #region Variables
        //------------------------------------------------------------------------------------------------------------------------
        ILed led;
        //------------------------------------------------------------------------------------------------------------------------
        #endregion
        #region Constructor
        //------------------------------------------------------------------------------------------------------------------------
        public Led(GrovePi.Pin pin)
        {
            led = DeviceFactory.Build.Led(pin);
        }
        //------------------------------------------------------------------------------------------------------------------------
        #endregion
        #region Functions
        //------------------------------------------------------------------------------------------------------------------------
        public void SetBrightness(int val)
        {
            led.AnalogWrite((byte)val);
        }
        //------------------------------------------------------------------------------------------------------------------------
        #endregion
    }
}
